    private void sequenceInputFieldFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_sequenceInputFieldFocusLost
        String text1 = sequence1InputField.getText();
        String text2 = sequence2InputField.getText();
        if (!(text1.equals(model.sequence1) && text2.equals(model.sequence2))
                && text1.length() > 0 && text2.length() > 0)
        {
            model = new DPModel(text1, text2);
            gridView = new GridView(model);
            alignmentView = new AlignmentView(model);
            gridViewport.setView(gridView);
            alignmentViewport.setView(alignmentView);
            fillStepForwardButton.setEnabled(true);
            fillJumpForwardButton.setEnabled(true);
            fillStepBackButton.setEnabled(false);
            fillJumpBackButton.setEnabled(false);
            traceStepForwardButton.setEnabled(false);
            traceJumpForwardButton.setEnabled(false);
            traceStepBackButton.setEnabled(false);
            traceJumpBackButton.setEnabled(false);
            clearButton.setEnabled(true);
        }
    }//GEN-LAST:event_sequenceInputFieldFocusLost

