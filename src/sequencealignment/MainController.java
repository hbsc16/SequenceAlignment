    private void fillStepBackButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_fillStepBackButtonActionPerformed
        model.clearCellAndDecrement();
        
        gridView.updateAll();
        
        // if beginning reached, disable back fill buttons
        if (model.x == 1 && model.y == 1)
        {
            fillStepBackButton.setEnabled(false);
            fillJumpBackButton.setEnabled(false);
        }
        
        fillStepForwardButton.setEnabled(true);
        fillJumpForwardButton.setEnabled(true);
        traceStepForwardButton.setEnabled(false);
        traceJumpForwardButton.setEnabled(false);
    }//GEN-LAST:event_fillStepBackButtonActionPerformed

    private void fillStepForwardButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_fillStepForwardButtonActionPerformed
        model.fillCellAndIncrement();

        gridView.updateAll();
        
        // if end reached, reset coordinates and disable forward buttons
        if (model.x == model.sequence2.length() && model.y == model.sequence1.length()
                && model.path[model.y][model.x] != 0)
        {
            fillStepForwardButton.setEnabled(false);
            fillJumpForwardButton.setEnabled(false);
            traceStepForwardButton.setEnabled(true);
            traceJumpForwardButton.setEnabled(true);
        }
        
        // enable fill back buttons
        fillStepBackButton.setEnabled(true);
        fillJumpBackButton.setEnabled(true);
        
    }//GEN-LAST:event_fillStepForwardButtonActionPerformed

